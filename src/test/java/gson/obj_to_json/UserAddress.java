package gson.obj_to_json;

public class UserAddress {  
    String street;
    String houseNumber;
    String city;
    String country;
    
    
	public UserAddress(String street, String houseNumber, String city,
			String country) {
		super();
		this.street = street;
		this.houseNumber = houseNumber;
		this.city = city;
		this.country = country;
	}

    
}
